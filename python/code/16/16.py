# 1. Iterating through a list:
fruits = ["apple", "banana", "cherry"]
for fruit in fruits:
    print(fruit)

# 2. Iterating through a string (sequence of characters):
for char in "hello":
    print(char)

# 3. Using the range() function:
# The range() function generates a sequence of numbers. It can be used with a
# for loop to execute a block of code a certain number of times.

for i in range(5):  # This will print numbers 0 to 4
    print(i)
