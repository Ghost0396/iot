"""
Comparison Operators
"""

# Equal to (==): Checks if the value of two operands is equal.
print(5 == 5)  # Output: True
print(5 == 3)  # Output: False

# Not equal to (!=): Checks if the value of two operands is not equal.
print(5 != 5)  # Output: False
print(5 != 3)  # Output: True

# Greater than (>): Checks if the value of the left operand is greater than
# the value of the right operand.
print(5 > 3)  # Output: True
print(5 > 5)  # Output: False

# Less than (<): Checks if the value of the left operand is less than the
# value of the right operand.
print(3 < 5)  # Output: True
print(5 < 5)  # Output: False

# Greater than or equal to (>=): Checks if the value of the left operand is
# greater than or equal to the value of the right operand.
print(5 >= 5)  # Output: True
print(3 >= 5)  # Output: False

# Less than or equal to (<=): Checks if the value of the left operand is less
# than or equal to the value of the right operand
print(5 <= 5)  # Output: True
print(6 <= 5)  # Output: False


"""
Strings
"""

# Equal to (==): Checks if two strings are identical.
print("hello" == "hello")  # Output: True
print("Hello" == "hello")  # Output: False (case sensitive)

# Greater than (>): Checks if the left string comes after the right string
# lexicographically.
print("hello" > "apple")  # Output: True
print("apple" > "banana")  # Output: False

