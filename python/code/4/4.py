"""
Arithmetic Operators
"""

# Adds two operands
x = 5
y = 3
print(x + y)  # Output: 8

# Subtracts second operand from the first
x = 5
y = 3
print(x - y)  # Output: 2

# Multiplies two operands
x = 5
y = 3
print(x * y)  # Output: 15

# Divides the first operand by the second
x = 15
y = 3
print(x / y)  # Output: 5.0

# Returns the remainder when first operand is divided by the second
x = 14
y = 3
print(x % y)  # Output: 2

# Raises the first operand to the power of the second
x = 2
y = 3
print(x ** y)  # Output: 8

# Performs floor division, the division that results in the whole number
# adjusted to the left in the number line
x = 15
y = 2
print(x // y)  # Output: 7

"""
Integers and Floats
"""

# Adds two operands
int_num = 10
float_num = 3.5
result = int_num + float_num  # Result will be a float
print(result)  # Output: 13.5

# Subtracts second operand from the first
int_num = 10
float_num = 3.5
result = int_num - float_num  # Result will be a float
print(result)  # Output: 6.5

# Multiplies two operands
int_num = 10
float_num = 3.5
result = int_num * float_num  # Result will be a float
print(result)  # Output: 35.0

# Divides the first operand by the second (result is always a float)
int_num = 10
float_num = 4.0
result = int_num / float_num
print(result)  # Output: 2.5

"""
Strings
"""

# Concatenates two strings
string1 = "Hello"
string2 = "World"
result = string1 + " " + string2  # Strings are concatenated with a space
print(result)  # Output: "Hello World"

# Repeats the string
string = "Repeat"
times = 3
result = string * times  # String is repeated 3 times
print(result)  # Output: "RepeatRepeatRepeat"
