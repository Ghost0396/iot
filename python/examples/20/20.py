"""
Tuples
"""

my_tuple = (1, 2, 3, 2, 4, 2)

# Count
count = my_tuple.count(2)  # Counts how many times 2 appears in the tuple
print(count)  # Output: 3

# Index
index = my_tuple.index(2)  # Finds the index of the first occurrence of 2
print(index)  # Output: 1


